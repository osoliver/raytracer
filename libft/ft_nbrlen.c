#include <string.h>

size_t		ft_nbrlen(int n)
{
	size_t		len;

	len = (n < 0) ? 2 : 1;
	n = (n < 0) ? -n : n;
	while (n > 9)
	{
		len++;
		n /= 10;
	}
	return (len);
}
