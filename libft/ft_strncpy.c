#include "libft.h"

char		*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t		i;

	i = 0;
	while (i < n && s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	ft_bzero(s1 + i, n - i);
	return (s1);
}
