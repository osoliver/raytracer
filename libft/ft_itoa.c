#include "libft.h"

char		*ft_itoa(int n)
{
	char		*c;
	int			nb;
	size_t		i;

	nb = n;
	i = (nb < 0) ? 2 : 1;
	while (++i && (nb /= 10))
		(void)n;
	if (!(c = ft_strnew(i)))
		return (NULL);
	c[--i] = '\0';
	c[0] = (n < 0) ? '-' : '0';
	c[--i] = ((n % 10) * ((n < 0) ? -1 : 1)) + '0';
	n /= 10 * ((n < 0) ? -1 : 1);
	while (n)
	{
		c[--i] = n % 10 + '0';
		n /= 10;
	}
	return (c);
}
