#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char		*s2;
	size_t		i;

	if (!(s2 = ft_strnew(sizeof(*s1) * (ft_strlen(s1) + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = 0;
	return (s2);
}
