#include "libft.h"

char		*ft_strcpy2(char *s1, const char *s2, const char *s3)
{
	size_t		i;

	i = 0;
	while (s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	while (*s3)
		s1[i++] = *(s3++);
	s1[i] = 0;
	return (s1);
}
