#include <stdlib.h>

void			ft_freechar2(char ***c2)
{
	size_t	i;

	i = 0;
	while ((*c2)[i])
	{
		free((*c2)[i]);
		(*c2)[i++] = NULL;
	}
	free(*c2);
	*c2 = NULL;
}
